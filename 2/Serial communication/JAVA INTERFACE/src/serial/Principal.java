    private void lcd(char linha){
        int i;
        if(linha == '1'){
            try {
                sport.write('l');
                Thread.sleep(50);
                sport.write('1');
                Thread.sleep(50);
                for(i=0 ;i<txtlcdl1.getText().length();i++){
                    sport.write(txtlcdl1.getText().charAt(i));
                    Thread.sleep(50);
                }
                for(i=txtlcdl1.getText().length();i<16;i++){
                    sport.write(' ');
                    Thread.sleep(50);
                }
            } catch (Exception e) {
                  System.err.println("Error: Write port failed: " + e);
            }
        }else if(linha == '2'){
            try {
                sport.write('l');
                Thread.sleep(50);
                sport.write('2');
                Thread.sleep(50);
                for(i=0 ;i<txtlcdl2.getText().length();i++){
                    sport.write(txtlcdl2.getText().charAt(i));
                    Thread.sleep(50);
                }
                for(i=txtlcdl2.getText().length();i<16;i++){
                    sport.write(' ');
                    Thread.sleep(50);
                }
            } catch (Exception e) {
                  System.err.println("Error: Write port failed: " + e);
            }
        }
    }
