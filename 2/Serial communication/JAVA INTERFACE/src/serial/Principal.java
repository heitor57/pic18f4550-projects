    private void btconecdescActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btconecdescActionPerformed

        if(conectado == false){
            try {
              sport.open("COM"+spporta.getValue());
              conectado = true;
              btconecdesc.setText("Desconectar");
              timer.start();
              
            } catch (Exception e) {
              System.err.println("Error: open port failed: " + e);
            }
        }else if(conectado == true){
            try {
              sport.close();
              conectado = false;
              btconecdesc.setText("Conectar");
              timer.stop();
              rb_b0.getModel().setSelected(false);
              rb_b1.getModel().setSelected(false);
              rb_b2.getModel().setSelected(false);
              rb_b3.getModel().setSelected(false);
              rb_b4.getModel().setSelected(false);
              rb_b5.getModel().setSelected(false);
              rb_b6.getModel().setSelected(false);
              rb_b7.getModel().setSelected(false);
              rb_d0.getModel().setSelected(false);
              rb_d1.getModel().setSelected(false);
              rb_d2.getModel().setSelected(false);
              rb_d3.getModel().setSelected(false);
              rb_d4.getModel().setSelected(false);
              rb_d5.getModel().setSelected(false);
              rb_d6.getModel().setSelected(false);
              rb_d7.getModel().setSelected(false);
              rb_c5.getModel().setSelected(false);
              lbltemperatura.setText("Temperatura");
              progresst.getModel().setValue(0);
              sliderc.getModel().setValue(0);
              progressc.getModel().setValue(0);
              txtlcdl1.setText("");
              txtlcdl2.setText("");
            } catch (Exception e) {
              System.err.println("Error: open port failed: " + e);
            }
        }
    }//GEN-LAST:event_btconecdescActionPerformed

